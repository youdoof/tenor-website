function toggleShowMusic() {
    var content = document.getElementById("music-container");
    if (content.style.display === "block") {
        content.style.display = "none";
    } else {
        content.style.display = "block";
    }
}

function toggleShowResources() {
    var content = document.getElementById("resources-container");
    if (content.style.display === "block") {
        content.style.display = "none";
    } else {
        content.style.display = "block";
    }
}
